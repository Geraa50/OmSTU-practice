// Подключаем System для работы с консолью
using System;
// Подключаем System.IO для работы с файлами (чтение/запись)
using System.IO;

class Program
{
    static void Main()
    {
        // Просим пользователя ввести путь к файлу с исходными строками
        Console.Write("Введите путь к файлу: ");
        string path = Console.ReadLine();

        // Проверяем, существует ли файл по указанному пути
        if (!File.Exists(path))
        {
            Console.WriteLine("Файл не найден!");
            return; // Завершаем программу, если файла нет
        }

        // Считываем все строки из файла в массив строк
        // Каждый элемент массива — одна строка из файла
        string[] lines = File.ReadAllLines(path);

        // Если файл пустой — сообщаем и выходим
        if (lines.Length == 0)
        {
            Console.WriteLine("Файл пуст!");
            return;
        }

        // Переменная для хранения глобального максимума
        // (наибольшее количество подряд идущих одинаковых элементов среди ВСЕХ строк)
        int globalMax = 0;

        // Массив для хранения максимального количества подряд идущих
        // одинаковых элементов для КАЖДОЙ строки
        int[] maxCounts = new int[lines.Length];

        // Проходим по каждой строке файла
        for (int i = 0; i < lines.Length; i++)
        {
            string line = lines[i];

            // Если строка пустая — записываем 0 и переходим к следующей
            if (line.Length == 0)
            {
                maxCounts[i] = 0;
                continue; // Переходим к следующей итерации цикла
            }

            // Для текущей строки: максимальная длина подряд идущих одинаковых символов
            int maxCount = 1;
            // Текущий счётчик подряд идущих одинаковых символов
            int currentCount = 1;

            // Проходим по символам строки, начиная со второго (индекс 1)
            for (int j = 1; j < line.Length; j++)
            {
                // Если текущий символ совпадает с предыдущим
                if (line[j] == line[j - 1])
                {
                    // Увеличиваем счётчик текущей последовательности
                    currentCount++;
                    // Если текущая последовательность стала длиннее максимальной — обновляем
                    if (currentCount > maxCount)
                    {
                        maxCount = currentCount;
                    }
                }
                else
                {
                    // Если символы различаются — сбрасываем счётчик на 1
                    currentCount = 1;
                }
            }

            // Сохраняем максимум для этой строки
            maxCounts[i] = maxCount;

            // Если максимум этой строки больше глобального — обновляем глобальный
            if (maxCount > globalMax)
            {
                globalMax = maxCount;
            }
        }

        // Выводим результат: глобальный максимум
        Console.WriteLine("\nМаксимальное количество подряд идущих одинаковых элементов: " + globalMax);
        Console.WriteLine("Строки с таким количеством:");

        // Выводим все строки, у которых максимум совпадает с глобальным
        for (int i = 0; i < lines.Length; i++)
        {
            if (maxCounts[i] == globalMax)
            {
                Console.WriteLine(lines[i]);
            }
        }
    }
}

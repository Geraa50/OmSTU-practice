// Подключаем System для консольного ввода/вывода
using System;
// Подключаем System.Collections.Generic для работы с List<char>
using System.Collections.Generic;

// Класс стека для символов (char) — используем для хранения открывающих скобок
// Это "чистый" стек — реализуем все методы вручную через List
class MyStack
{
    // Внутренний список для хранения элементов стека
    private List<char> items = new List<char>();

    // Метод Push — добавляет элемент на вершину стека
    public void Push(char item)
    {
        items.Add(item); // Добавляем в конец списка (вершина стека)
    }

    // Метод Pop — извлекает верхний элемент с удалением
    public char Pop()
    {
        // Если стек пуст — возвращаем специальный символ '\0' (нулевой символ)
        if (items.Count == 0)
        {
            return '\0';
        }
        // Сохраняем верхний элемент
        char item = items[items.Count - 1];
        // Удаляем его из списка
        items.RemoveAt(items.Count - 1);
        // Возвращаем сохранённый элемент
        return item;
    }

    // Метод Peek — просмотр верхнего элемента без удаления
    public char Peek()
    {
        if (items.Count == 0)
        {
            return '\0';
        }
        return items[items.Count - 1];
    }

    // Метод IsEmpty — проверяет, пуст ли стек
    public bool IsEmpty()
    {
        return items.Count == 0; // Возвращает true, если элементов нет
    }
}

class Program
{
    static void Main()
    {
        // Просим пользователя ввести математическое выражение
        Console.Write("Введите математическое выражение: ");
        string expression = Console.ReadLine();

        // Создаём стек для хранения открывающих скобок
        MyStack stack = new MyStack();

        // Флаг корректности выражения
        bool isValid = true;

        // Проходим по каждому символу выражения
        for (int i = 0; i < expression.Length; i++)
        {
            char c = expression[i]; // Текущий символ

            // Если символ — открывающая скобка (любого из трёх видов)
            if (c == '(' || c == '[' || c == '{')
            {
                // Добавляем открывающую скобку в стек
                stack.Push(c);
            }
            // Если символ — закрывающая скобка
            else if (c == ')' || c == ']' || c == '}')
            {
                // Сначала проверяем, не пуст ли стек
                // Если стек пуст — значит нет соответствующей открывающей скобки
                if (stack.IsEmpty())
                {
                    Console.WriteLine("Ошибка! Найдена закрывающая скобка '" + c +
                                      "' на позиции " + (i + 1) + ", но нет соответствующей открывающей.");
                    isValid = false;
                    break; // Прерываем проверку — ошибка найдена
                }

                // Смотрим, что лежит на вершине стека (последняя открывающая скобка)
                char top = stack.Peek();

                // Проверяем, совпадает ли пара скобок:
                // ')' должна соответствовать '('
                // ']' должна соответствовать '['
                // '}' должна соответствовать '{'
                if ((c == ')' && top == '(') ||
                    (c == ']' && top == '[') ||
                    (c == '}' && top == '{'))
                {
                    // Если пара совпала — удаляем открывающую скобку из стека
                    stack.Pop();
                }
                else
                {
                    // Если пара не совпала — выводим ошибку
                    Console.WriteLine("Ошибка! Скобка '" + c + "' на позиции " + (i + 1) +
                                      " не соответствует открывающей скобке '" + top + "'.");
                    isValid = false;
                    break; // Прерываем проверку
                }
            }
            // Все остальные символы (числа, операторы, буквы) — игнорируем
        }

        // После прохода по всему выражению проверяем, пуст ли стек
        // Если стек НЕ пуст — значит остались незакрытые открывающие скобки
        if (isValid && !stack.IsEmpty())
        {
            Console.WriteLine("Ошибка! Остались незакрытые скобки.");
            isValid = false;
        }

        // Если все проверки пройдены — выражение корректно
        if (isValid)
        {
            Console.WriteLine("Выражение корректно! Все скобки расставлены правильно.");
        }
    }
}
